var tab = ["chaima"];

// add item at the last position in tab
console.log(tab);

tab.push("aissaoui");

console.log(tab);

// add item at the first position in tab

tab.unshift("21 yo");
console.log(tab);

// remove item at the last position from tab
tab.pop();

console.log(tab);

// remove item at the first position from tab

tab.shift();
console.log(tab);
